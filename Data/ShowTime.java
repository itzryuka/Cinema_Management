public class ShowTime {
    String id;
    String name;
    String roomId;
    String nameroom;
    String type;
    String date;
    String time;
    String nprice;
    String vprice;

    public ShowTime(String id, String name, String roomId, String nameroom, String type, String date, String time, String nprice, String vprice) {
        this.id = id;
        this.name = name;
        this.roomId = roomId;
        this.nameroom = nameroom;
        this.type = type;
        this.date = date;
        this.time = time;
        this.nprice = nprice;
        this.vprice = vprice;
    }
}
