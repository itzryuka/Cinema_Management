public class Movie {
    String id;
    String name;
    String genre;
    String duration;
    String age;
    String price;
    public Movie(String id, String name, String genre, String duration, String age, String price) {
        this.id = id;
        this.name = name;
        this.genre = genre;    
        this.duration = duration;
        this.age = age;
        this.price = price;
    }
}